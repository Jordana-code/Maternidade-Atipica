// RELATOS DINÂMICOS NA HOME — Seção "Relatos de quem sabe o que você passa"
// Busca os 2 relatos mais recentes do Supabase e substitui os fallbacks.
// Se não houver relatos ainda, os cards estáticos de fallback permanecem.
// =====================================================================

async function carregarRelatosHome() {
    const grid = document.getElementById('grid-relatos-home');
    if (!grid) return;

    try {
        const { data: relatos, error } = await supabaseClient
            .from('relatos')
            .select('id, usuario_id, titulo, nome_crianca, texto, criado_em')
            .order('criado_em', { ascending: false })
            .limit(2);

        if (error || !relatos || relatos.length === 0) return;

        // Busca os nomes dos autores em paralelo
        const perfis = await Promise.all(
            relatos.map(r =>
                supabaseClient
                    .from('perfis')
                    .select('nome')
                    .eq('id', r.usuario_id)
                    .single()
                    .then(({ data }) => data || { nome: 'Mãe da Comunidade' })
            )
        );

        // Remove os fallbacks e injeta os relatos reais com animação
        const fallbacks = grid.querySelectorAll('.depoimento-fallback');
        fallbacks.forEach(el => el.remove());

        relatos.forEach((relato, i) => {
            const autor = perfis[i];
            const nomeCurto = autor.nome || 'Mãe da Comunidade';

            // Iniciais para o avatar
            const partes = nomeCurto.trim().split(' ');
            const iniciais = partes.length >= 2
                ? (partes[0][0] + partes[partes.length - 1][0]).toUpperCase()
                : partes[0].substring(0, 2).toUpperCase();

            // Trunca o texto para caber bem no card (máx 240 caracteres)
            const textoExibido = relato.texto.length > 240
                ? relato.texto.substring(0, 237).trimEnd() + '…'
                : relato.texto;

            // Citação de identificação (título ou nome da criança)
            const identificacao = relato.nome_crianca
                ? `${nomeCurto} — mãe de ${relato.nome_crianca}`
                : nomeCurto;

            const bloco = document.createElement('blockquote');
            bloco.className = 'depoimento-item depoimento-real';
            bloco.style.opacity = '0';
            bloco.style.transform = 'translateY(10px)';
            bloco.innerHTML = `
                <p>${escaparHTMLHome('"' + textoExibido + '"')}</p>
                <div class="depoimento-author">
                    <div class="avatar-depoimento">${iniciais}</div>
                    <cite>${escaparHTMLHome(identificacao)}</cite>
                </div>
                <a href="relatos.html" class="depoimento-link-ver" aria-label="Ver relato completo: ${escaparHTMLHome(relato.titulo)}">
                    Ver relato completo →
                </a>
            `;

            grid.appendChild(bloco);

            // Anima entrada com pequeno delay entre os cards
            setTimeout(() => {
                bloco.style.transition = 'opacity 0.5s ease, transform 0.5s ease';
                bloco.style.opacity = '1';
                bloco.style.transform = 'translateY(0)';
            }, i * 120);
        });

    } catch (e) {
        // Em caso de qualquer erro, os fallbacks estáticos já estão visíveis
        console.warn('Não foi possível carregar relatos da home:', e);
    }
}

/** Versão local de escaparHTML para não depender de comum.js no mesmo escopo de execução */
function escaparHTMLHome(texto) {
    const div = document.createElement('div');
    div.textContent = texto ?? '';
    return div.innerHTML;
}

// Aguarda o DOM e o Supabase estarem prontos
document.addEventListener('DOMContentLoaded', () => {
    // Pequeno delay para garantir que supabaseClient (de supabase-config.js) já foi criado
    setTimeout(carregarRelatosHome, 200);
});