// --- Sistema de Busca Dinâmica ---
const campoBusca = document.getElementById('campo-busca');
const listaCards = document.querySelectorAll('.card');
const alertaBuscaVazia = document.getElementById('busca-vazia');

campoBusca.addEventListener('input', () => {
    const termoPesquisa = campoBusca.value.trim().toLowerCase();
    let encontrouResultados = false;

    listaCards.forEach(card => {
        const palavrasChave = card.getAttribute('data-tags');
        const tituloCard = card.querySelector('h3').textContent.toLowerCase();
        const textoCard = card.querySelector('p').textContent.toLowerCase();

        if (palavrasChave.includes(termoPesquisa) || tituloCard.includes(termoPesquisa) || textoCard.includes(termoPesquisa)) {
            card.classList.remove('escondido');
            encontrouResultados = true;
        } else {
            card.classList.add('escondido');
        }
    });

    alertaBuscaVazia.classList.toggle('escondido', encontrouResultados);
});

// --- Sistema de Janela Modal Acessível (Artigos) ---
const overlayModal = document.getElementById('modal-dinamico');
const caixaConteudoInterno = document.getElementById('modal-conteudo-interno');
const botaoFecharModal = document.getElementById('fechar-modal-btn');
let ultimoElementoFocado = null;

function exibirArtigo(idArtigo) {
    const conteudoFormatado = acervoArtigos[idArtigo];
    if (conteudoFormatado) {
        ultimoElementoFocado = document.activeElement;
        caixaConteudoInterno.innerHTML = conteudoFormatado;
        overlayModal.classList.add('ativo');
        overlayModal.setAttribute('aria-hidden', 'false');
        document.body.style.overflow = 'hidden';
        setTimeout(() => { botaoFecharModal.focus(); }, 100);
    }
}

function fecharArtigo() {
    overlayModal.classList.remove('ativo');
    overlayModal.setAttribute('aria-hidden', 'true');
    document.body.style.overflow = '';
    setTimeout(() => { caixaConteudoInterno.innerHTML = ''; }, 300);
    if (ultimoElementoFocado) { ultimoElementoFocado.focus(); }
}

listaCards.forEach(card => {
    card.addEventListener('click', () => exibirArtigo(card.getAttribute('data-id')));
    card.addEventListener('keydown', (e) => {
        if (e.key === 'Enter' || e.key === ' ') {
            e.preventDefault();
            exibirArtigo(card.getAttribute('data-id'));
        }
    });
});

botaoFecharModal.addEventListener('click', fecharArtigo);
overlayModal.addEventListener('click', (e) => { if (e.target === overlayModal) fecharArtigo(); });

document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape' && overlayModal.classList.contains('ativo')) { fecharArtigo(); }
});
